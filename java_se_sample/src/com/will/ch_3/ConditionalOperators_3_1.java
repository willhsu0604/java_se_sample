package com.will.ch_3;

public class ConditionalOperators_3_1 {

	public static void main(String[] args) {
		
		int a = 3;
		int b = 3;
		int c = 4;
		
		System.out.println("a == b && a == c : " + (a == b && a == c));
		System.out.println("a == b || a == c : " + (a == b || a == c));
		System.out.println("!(a == b) : " + !(a == b));
		
	}
	
}
