package com.will.ch_3;

public class PrimitiveDataType_3_1 {
	
	public static void main(String[] args) {
		
		// calculating by values
		int result1 =  3 * 5;
		System.out.println("3 * 5 = " + result1);
		
		// calculating by variables
		int a = 5;
		int b = 3;
		int result2 =  a * b;
		System.out.println("a * b = " + result2);
		
	}
	
}
