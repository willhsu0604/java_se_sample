package com.will.ch_3;

public class Literal_3_2 {
	
	public static void main(String[] args) {
		// float
		System.out.println("3.14f => " + 3.14f);
		System.out.println("3.14F => " + 3.14F);
		
		// double
		System.out.println("3.14 => " + 3.14);

		// 科學記號
		System.out.println("1.23e-10 => " + 1.23e-10);
		
	}
	
}
